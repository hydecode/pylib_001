# This is an example Python function library.

# creating a simple add function
def add(a, b):
    return a+b

# creating a simple division function
def divide(a, b):
    return a/b

# creating a simple odd_even function
# to check if the number is odd or even
def odd_even(n):
    if n % 2 == 0:
        return("Even")
    else:
        return("Odd")
